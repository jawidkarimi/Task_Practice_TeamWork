package src.Senai.week_2_Java_Coding;

public class SWAP {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;

        x = 10 + 3;  // x + y
        y = 13 - 3;  // x - y
        x = 13 - 10;  // x - y

        System.out.println("x = " + x);
        System.out.println("y = " + y);

        System.out.println("------------------------------");

        int x1 = 12;
        int y1 = 4;

        int z = x1;

        x1 = y1;
        y1 = z ;

        System.out.println("x1 = " + x1);
        System.out.println("y1 = " + y1);

    }
}
